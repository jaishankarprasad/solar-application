package com.rainiersoft.solar.entity;

public class ACAndDCPower 
{
	String acPower;
	String dcPower;
	String dateAndTime;


	public String getAcPower() {
		return acPower;
	}
	public void setAcPower(String acPower) {
		this.acPower = acPower;
	}
	public String getDcPower() {
		return dcPower;
	}
	public void setDcPower(String dcPower) {
		this.dcPower = dcPower;
	}
	public String getDateAndTime() {
		return dateAndTime;
	}
	public void setDateAndTime(String dateAndTime) {
		this.dateAndTime = dateAndTime;
	}



}
